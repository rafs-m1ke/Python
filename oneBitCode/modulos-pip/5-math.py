import math

# 1- Acessar o número PI
print(math.pi)
print(f"{math.pi:.2f}")

# 2- Acessar número de Euler
print(math.e)
print(f"{math.e:.2f}")

# 3- Arredondando número para cima e para baixo
num1 = 10.4

print(math.ceil(num1))
print(math.floor(num1))

# 4- Fatorial de número
num2 = 5
print(math.factorial(num2))

# 5- Potência de números
print(math.pow(5,2))

# 6- Raíz quadrade de um número
print(math.sqrt(169))

# 7- MDC
mdc = math.gcd(20, 70) # 25/100
print(mdc)

# 8- Logarítmo
print(math.log(10))
